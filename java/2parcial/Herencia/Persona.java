public class Persona{

protected String nombre,sexo;
protected int edad;

public void setNombre(String nombre){
this.nombre=nombre;
}

public void setEdad(int edad){
this.edad=edad;
}
public int getEdad(){
return edad;
}
public String getNombre(){
return nombre;
}

public void setSexo(String sexo){
this.sexo=sexo;
}
public String getSexo(){
return sexo;
}




}