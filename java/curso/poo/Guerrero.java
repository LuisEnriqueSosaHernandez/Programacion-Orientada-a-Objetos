public class Guerrero{
private String Nombre;
private int fuerza;
private int salud;

public String getNombre(){
return Nombre;

}

public void setNombre(String Nombre){
this.Nombre=Nombre; 
}

public int getfuerza(){
return fuerza;

}

public void setfuerza(int fuerza){
this.fuerza=fuerza; 
}

public int getsalud(){
return salud;

}

public void setsalud(int salud){
this.salud=salud; 
}

}