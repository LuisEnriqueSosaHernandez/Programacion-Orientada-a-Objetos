import java.util.Scanner;
public class Tipos{
public static void main(String[]arg){
char letra;
letra='t';
switch(letra){
case 'b':
System.out.println("El valor es b");
break;

case 'a':
System.out.println("El valor es a");
break;

case '1':
System.out.println("El valor es 1");
break;

case '%':
System.out.println("El valor es %");
break;
default:
System.out.println("No esta el valor en case");
}

}


}